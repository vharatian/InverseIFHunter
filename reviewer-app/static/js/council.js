let _getSessionId = () => null;
let _onApprove = null;
let _state = { running: false, rules: {}, ruleOrder: [], abortCtrl: null, chairman: {} };

export function initCouncil(getSessionId, onApprove) {
  _getSessionId = getSessionId;
  _onApprove = onApprove;

  const runBtn = document.getElementById("btn-run-council");
  if (runBtn) {
    runBtn.addEventListener("click", () => {
      const sid = _getSessionId();
      if (sid) runCouncil(sid);
      else {
        const nbOnly = document.getElementById("task-panel")?.dataset?.notebookOnly === "true";
        showToast(
          nbOnly
            ? "Council only runs for a linked training session. This view is from the notebook file only."
            : "Load a task (session ID from the trainer queue) first.",
          "info",
        );
      }
    });
  }

  const stopBtn = document.getElementById("btn-stop-council");
  if (stopBtn) {
    stopBtn.addEventListener("click", () => {
      if (_state.abortCtrl) _state.abortCtrl.abort();
    });
  }

  const rulesBtn = document.getElementById("btn-council-rules");
  if (rulesBtn) {
    rulesBtn.addEventListener("click", _showRulesModal);
  }
}

export function resetCouncil() {
  if (_state.abortCtrl) _state.abortCtrl.abort();
  _state = { running: false, rules: {}, ruleOrder: [], abortCtrl: null, chairman: {} };

  const bar = document.getElementById("council-bar");
  if (bar) bar.className = "council-bar council-bar--idle";

  const slots = document.getElementById("council-slots");
  if (slots) { slots.innerHTML = ""; slots.hidden = true; }

  const detail = document.getElementById("council-detail");
  if (detail) { detail.innerHTML = ""; detail.hidden = true; }

  const summary = document.getElementById("council-summary");
  if (summary) summary.textContent = "";

  const banner = document.getElementById("triage-banner");
  if (banner) { banner.innerHTML = ""; banner.hidden = true; }

  const taskContent = document.getElementById("task-content");
  if (taskContent) taskContent.classList.remove("task-content--collapsed");

  _showRunBtn();
  document.querySelectorAll(".council-badge").forEach((b) => b.remove());
  document.querySelectorAll("[data-council-border]").forEach((el) => el.removeAttribute("data-council-border"));
}

