export function showRulesModal() {
  const rows = RULES_REFERENCE.map((r) => {
    const typeBadge = r.type === "deterministic"
      ? `<span class="rules-type-badge rules-type-badge--det">Deterministic</span>`
      : `<span class="rules-type-badge rules-type-badge--llm">LLM Council</span>`;
    return `<div class="rules-ref-row">
      <div class="rules-ref-header"><span class="rules-ref-id">${escapeHtml(r.id)}</span>${typeBadge}</div>
      <div class="rules-ref-desc">${escapeHtml(r.description)}</div>
      <div class="rules-ref-details">
        <div class="rules-ref-detail"><span class="rules-ref-label">What it checks</span><p>${escapeHtml(r.what_it_checks)}</p></div>
        <div class="rules-ref-detail"><span class="rules-ref-label">How it works</span><p>${escapeHtml(r.how_it_works)}</p></div>
        <div class="rules-ref-detail"><span class="rules-ref-label">Pass condition</span><p>${escapeHtml(r.pass_condition)}</p></div>
        <div class="rules-ref-detail rules-ref-detail--fail"><span class="rules-ref-label">Failure example</span><p>${escapeHtml(r.fail_example)}</p></div>
      </div>
    </div>`;
  }).join("");

  showModal("Council Rules Reference", `<div class="rules-ref-list">
    <p class="rules-ref-intro">Each rule is evaluated per task. LLM council rules use 4 models to vote, then the Chairman makes the final PASS/FAIL decision.</p>
    ${rows}
  </div>`);
}
